Diferença entre 1:1 e 1:N

Usamos a FK (foreign key) na tabela com os registros filhos em ambos os casos. 
A diferença é que na relação 1:1, cada registro da tabela tem valores de FK únicos. 
Cada registro pai tem um link para apenas um registro filho. Na relação 1:N, podemos 
ter mais de um registro com a mesma FK. 

